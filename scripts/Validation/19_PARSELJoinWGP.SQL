FROM PASSAGIER, BUCHUNG
WHERE (PASSAGIER.PNR = BUCHUNG.PNR)
AND (BUCHUNG.BNR >= 500)
AND (BUCHUNG.BNR <= 1500)
GROUP BY FLC;

