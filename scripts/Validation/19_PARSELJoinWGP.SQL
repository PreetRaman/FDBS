FROM PASSAGIER, BUCHUNG
WHERE (PASSAGIER.PNR = BUCHUNG.PNR)
AND (BUCHUNG.VON = 'FRA')
AND (BUCHUNG.NACH = 'CDG')
GROUP BY FLC;

/* COUNT GROUP BY */
